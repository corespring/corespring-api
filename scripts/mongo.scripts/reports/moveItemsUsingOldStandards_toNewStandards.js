function moveItem(mapping,item){
  var standards = item.standards;
  //print(standards);
  var newStandards = [];

  var newStandard = db.ccstandards_new.findOne( { uri: mapping.destinationUrl + "/" });
  
  if(newStandard){
    
    for( var i = 0 ; i < standards.length ; i++ ){
      var s = standards[i];
      //print(typeof(s) + " id: " + s);

      if(s.toString() != mapping.sourceId){
        newStandards.push( new ObjectId(s.toString()));
      }
    }

    newStandards.push(newStandard._id);
    item.standards = newStandards;
    db.content.save(item);
    print("completed for item: item:" + item._id);
  } else {
    print("couldn't find destination standard: " + mapping.destinationUrl);
  }
}


for( var i = 0 ; i < mappings.length ; i++ ){
  var m = mappings[i];

  if(m.destinationUrl){
    db.content.find( { standards: ObjectId(m.sourceId)}).forEach( function(item){ 
      moveItem(m, item); 
    }
    );
  }
  else {
    print("can't process item with id: " + m.sourceId);
  }
}

